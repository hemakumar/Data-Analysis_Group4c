shade n = case n of
            3 -> catMaybes $ [shd' !? 1]++[shd !? 5]++[shd' !? 2]
            4 -> catMaybes $ [shd' !? 0]++[shd !? i | i<-[3,7]]++[shd' !? 3]
            5 -> catMaybes $ [shd' !? 0]++[shd !? i | i<-[3,5,7]]++[shd' !? 3]
            6 -> catMaybes $ [shd !? 1 ]++[shd' !? 1]++[shd !? i | i <-[4,6]]++[shd' !? 2]++[shd !? 9]
            7 -> catMaybes $ [shd !? 1 ]++[shd' !? 1]++[shd !? i | i <-[4..6]]++[shd' !? 2]++[shd !? 9]
            8 -> catMaybes $ [shd !? i | i<-[1..4]++[6..9]]
            9 -> catMaybes $ [shd !? i | i<-[1..9]]
            10 -> catMaybes [shd !? i | i<-[0..4]++[6..10]]
            11 -> catMaybes [shd !? i | i<-[0..10]]
            _ -> []
